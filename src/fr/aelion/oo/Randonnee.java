package fr.aelion.oo;

public class Randonnee {
	private String nom;
	private int niveauDifficulte;
	private int ageMini;
	private int ageMaxi;
	private int nbKilometres;
	
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public int getNiveauDifficulte() {
		return niveauDifficulte;
	}
	public void setNiveauDifficulte(int niveauDifficulte) {
		this.niveauDifficulte = niveauDifficulte;
	}
	public int getAgeMini() {
		return ageMini;
	}
	public void setAgeMini(int ageMini) {
		this.ageMini = ageMini;
	}
	public int getAgeMaxi() {
		return ageMaxi;
	}
	public void setAgeMaxi(int ageMaxi) {
		this.ageMaxi = ageMaxi;
	}
	public int getNbKilometres() {
		return nbKilometres;
	}
	public void setNbKilometres(int nbKilometres) {
		this.nbKilometres = nbKilometres;
	}
	
	
}
